import { MedicalRecord } from "../models/medicalRecord.models.js";
import { User } from "../models/user.models.js";
import { uploadOnCloudinary } from "../utils/cloudinary.js";
import { ApiError } from "../utils/ApiError.js";
import { ApiResponse } from "../utils/ApiResponse.js";
import { asyncHandler } from "../utils/asyncHandler.js";

const uploadRecord = asyncHandler(async (req, res) => {
  // Use 'userIdentifier' which could be an ID or username
  const { userIdentifier, title } = req.body;
  const uploaderId = req.user?._id;

  if (!userIdentifier || !title) {
    throw new ApiError(400, "Patient Identifier and Title are required.");
  }

  // Find user by ID, username, or email natively.
  // In this model, they use 'fullname' mostly or '_id'. Let's search by ID or name regex.
  let patient;
  if (userIdentifier.match(/^[0-9a-fA-F]{24}$/)) {
    patient = await User.findById(userIdentifier);
  }

  if (!patient) {
    patient = await User.findOne({
      fullname: { $regex: new RegExp(userIdentifier, "i") },
      role: "PATIENT",
    });
  }

  if (!patient || patient.role !== "PATIENT") {
    throw new ApiError(
      404,
      "Invalid Patient provided. Please use an exact correct Patient ID or Patient Fullname."
    );
  }

  const fileLocalPath = req.file?.path;
  if (!fileLocalPath) {
    throw new ApiError(400, "Medical record file is required.");
  }

  // Define FileType dynamically based on MIME type natively generated by Multer
  let fileType = "FILE";
  if (req.file.mimetype.includes("pdf")) fileType = "PDF";
  else if (req.file.mimetype.includes("image")) fileType = "IMG";
  else if (
    req.file.mimetype.includes("zip") ||
    req.file.mimetype.includes("tar")
  )
    fileType = "ZIP";

  const uploadedFile = await uploadOnCloudinary(fileLocalPath);
  if (!uploadedFile) {
    throw new ApiError(500, "Failed to upload file to Cloudinary");
  }

  const record = await MedicalRecord.create({
    patientId: patient._id,
    title,
    fileUrl: uploadedFile.secure_url,
    fileType: fileType,
    fileSizeBytes: uploadedFile.bytes,
    uploadedBy: uploaderId,
  });

  // Re-fetch to populate
  const populatedRecord = await MedicalRecord.findById(record._id)
    .populate("patientId", "fullname email")
    .populate("uploadedBy", "fullname role");

  return res
    .status(201)
    .json(
      new ApiResponse(
        201,
        populatedRecord,
        "Medical Record uploaded successfully."
      )
    );
});

const getRecords = asyncHandler(async (req, res) => {
  const { patientId } = req.query;
  const userRole = req.user?.role;
  const userId = req.user?._id;

  let query = {};
  if (userRole === "PATIENT") {
    query.patientId = userId;
  } else if (patientId) {
    query.patientId = patientId;
  }

  const records = await MedicalRecord.find(query)
    .populate("patientId", "fullname email")
    .populate("uploadedBy", "fullname role")
    .sort({ createdAt: -1 });

  return res
    .status(200)
    .json(
      new ApiResponse(200, records, "Medical records fetched successfully.")
    );
});

export { uploadRecord, getRecords };
